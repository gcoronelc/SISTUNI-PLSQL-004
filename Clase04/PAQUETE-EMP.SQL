CREATE OR REPLACE PACKAGE SCOTT.PKG_EMP IS

TYPE T_CURSOR IS REF CURSOR;

PROCEDURE GET_EMPS(
  P_DEPTNO IN  SCOTT.EMP.DEPTNO%TYPE,
  P_CURSOR OUT T_CURSOR
);


END PKG_EMP;
/


CREATE OR REPLACE PACKAGE BODY SCOTT.PKG_EMP IS

PROCEDURE GET_EMPS(
  P_DEPTNO IN  SCOTT.EMP.DEPTNO%TYPE,
  P_CURSOR OUT T_CURSOR
) IS
BEGIN
  OPEN P_CURSOR FOR
  SELECT EMPNO, ENAME, SAL
  FROM SCOTT.EMP
  WHERE DEPTNO = P_DEPTNO;
END;


END PKG_EMP;
/


BEGIN
  SCOTT.PKG_EMP.GET_EMPS (10, :p_cursor$REFCURSOR);
END;







