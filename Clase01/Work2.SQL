CREATE OR REPLACE FUNCTION SCOTT.FNSALARIO
( P_CODIGO NUMBER )
RETURN NUMBER
IS
  V_SALARIO NUMBER;
BEGIN
  SELECT SAL INTO V_SALARIO
  FROM SCOTT.EMP
  WHERE EMPNO = P_CODIGO;
  RETURN V_SALARIO;
END;

