
CREATE OR REPLACE FUNCTION scott.fnDoble
( p_dato NUMBER )
RETURN NUMBER
IS
  v_doble NUMBER(8);
BEGIN
  v_doble := p_dato * 2;
  RETURN v_doble;
END;


